<?php
class ResponsaveisAssinaturas {
	
	public $idResponsaveisAssinaturas;
	public $oResponsaveis;
	public $oEmpresaCampanhaResponsaveis;
	public $cnpj;
	public $data_assinatura;
	public $tipo_documento;
	public $situacao;
	
	function __construct($idResponsaveisAssinaturas = NULL, Responsaveis $oResponsaveis = NULL, EmpresaCampanhaResponsaveis $oEmpresaCampanhaResponsaveis = NULL, $cnpj = NULL, $data_assinatura = NULL, $tipo_documento = NULL, $situacao = NULL){
		$this->idResponsaveisAssinaturas = $idResponsaveisAssinaturas;
		$this->oResponsaveis = $oResponsaveis;
		$this->oEmpresaCampanhaResponsaveis = $oEmpresaCampanhaResponsaveis;
		$this->cnpj = $cnpj;
		$this->data_assinatura = $data_assinatura;
		$this->tipo_documento = $tipo_documento;
		$this->situacao = $situacao;
	}
}