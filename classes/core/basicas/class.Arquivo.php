<?php
class Arquivo {
	
	public $idArquivo;
	public $nomeArquivo;
	public $novoNome;
	public $dataImportacao;
	public $situacao;
	public $status;
	public $dataHoraAlteracao;
	public $usuarioAlteracao;
	
	function __construct($idArquivo = NULL, $nomeArquivo = NULL, $novoNome = NULL, $dataImportacao = NULL, $situacao = NULL, $status = NULL, $dataHoraAlteracao = NULL, $usuarioAlteracao = NULL){
		$this->idArquivo = $idArquivo;
		$this->nomeArquivo = $nomeArquivo;
		$this->novoNome = $novoNome;
		$this->dataImportacao = $dataImportacao;
		$this->situacao = $situacao;
		$this->status = $status;
		$this->dataHoraAlteracao = $dataHoraAlteracao;
		$this->usuarioAlteracao = $usuarioAlteracao;
	}
}