<?php
class Arquivopolitica {
	
	public $idArquivoPol;
	public $oPoliticaambiental;
	public $nomeArquivo;
	public $novoNome;
	public $link;
	public $dataHoraAlteracao;
	public $usuarioAlteracao;
	
	function __construct($idArquivoPol = NULL, Politicaambiental $oPoliticaambiental = NULL, $nomeArquivo = NULL, $novoNome = NULL, $link = NULL, $dataHoraAlteracao = NULL, $usuarioAlteracao = NULL){
		$this->idArquivoPol = $idArquivoPol;
		$this->oPoliticaambiental = $oPoliticaambiental;
		$this->nomeArquivo = $nomeArquivo;
		$this->novoNome = $novoNome;
		$this->link = $link;
		$this->dataHoraAlteracao = $dataHoraAlteracao;
		$this->usuarioAlteracao = $usuarioAlteracao;
	}
}