<?php
class Origeminsumos {
	
	public $idOrigemInsumos;
	public $oEmpresa;
	public $oInsumos;
	public $quantidadeRegional;
	public $quantidadeNacional;
	public $quantidadeExterior;
	
	function __construct($idOrigemInsumos = NULL, Empresa $oEmpresa = NULL, Insumos $oInsumos = NULL, $quantidadeRegional = NULL, $quantidadeNacional = NULL, $quantidadeExterior = NULL){
		$this->idOrigemInsumos = $idOrigemInsumos;
		$this->oEmpresa = $oEmpresa;
		$this->oInsumos = $oInsumos;
		$this->quantidadeRegional = $quantidadeRegional;
		$this->quantidadeNacional = $quantidadeNacional;
		$this->quantidadeExterior = $quantidadeExterior;
	}
}